# app/utils.py


def format_answer(question: str, result):
    """
    Форматирует результат выполнения запроса в человеко-читаемый ответ.

    Parameters
    ----------
    question : str
        Вопрос, заданный пользователем на естественном языке.
    result : Any
        Результат выполнения Python-скрипта, сгенерированного моделью.
        Может быть строкой, числом, DataFrame и т.д.

    Returns
    -------
    str
        Строка с отформатированным ответом модели.

    Examples
    --------
    >>> format_answer("Какой средний доход у фрилансеров из Европы?", 78500)
    'Средний доход у фрилансеров из Европы: 78500'

    >>> format_answer("Какой процент фрилансеров-экспертов выполнили менее 100 проектов?", "11.13%")
    'Процент фрилансеров-экспертов, выполнивших менее 100 проектов: 11.13%'
    """
    return f"{result}"
